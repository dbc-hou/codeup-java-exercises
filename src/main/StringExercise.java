package main;

public class StringExercise {
    public static void main(String[] args) {
        String anotherBrick = "We don't need no education\nWe don't need no thought control";
        String internalQuotes = "Check \"this\" out!, \"s inside of \"s!";
        String mainDrive = "In windows, the main drive is usually C:\\";
        String backSlashes = "I can do blackslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(anotherBrick + "\n");
        System.out.println(internalQuotes + "\n");
        System.out.println(mainDrive + "\n");
        System.out.println(backSlashes + "\n");
    }
}
